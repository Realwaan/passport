                  <h1>Sectional Advanced Training (Wood Badge)</h1>
                  
              <p class="maintext">Course conducted in camp conditions. Focus is 
                on practical skills and simulated unit and sub-unit activities. 
                <strong>Participants must have received their Completion of Basic 
                Training Certificate</strong> and must have been at least 3 months 
                after taking the Basic Training. Upon completing the course participants 
                each receives a certificate of attendance.</p>
              <p class="maintext">In order to receive a certificate of completion 
                of advanced training or to receive the Wood Badge, participants 
                must complete their <strong>Training Studies</strong>. The training 
                studies is a series of questions a participant must answer through 
                further research and self-study. The training studies paper is 
                also fondly referred to as the thesis. </p>
              <p class="maintext">Upon submission of the training studies, the 
                participant will appear before a panel of Leader Trainers and 
                Assistant Leader Trainers for an interview. The panel's objective 
                is to ensure that the participant has a firm grasp of the fundamental 
                principles of the Scout movement.</p>
