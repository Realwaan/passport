                  <h1>Sectional Basic Training</h1>
                  
              <p class="maintext">It is a course aimed at equipping the participants 
                with the rudiments of managing a unit. It may be attended by actual 
                and/or prospective leaders. <strong>There are no pre-requisites 
                </strong>to take this course. Upon completing the course participants 
                each receives a certificate of attendance.</p>
              <p class="maintext">In order to receive a certificate of completion
                 of basic training, participants must complete their <strong>Training
                  Assignment </strong>. The training assignment is a paper assignment
                   where the participant must provide 3 months worth of unit
                  meeting  plan, 1 outdoor detailed activity-plan, proof of leadership
                  position,  and proof of advancing at least 25% of youth members
                  to the next  rank badge.</p>
