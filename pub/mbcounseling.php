                  <h1>Merit Badge Counseling</h1>
                  <p class="maintext">Any person who is at least 18 years of age 
                    and is willing to share his skills and know-how to help Scouts 
                    in earning a Merit Badge can serve as a Merit Badge Counselor. 
                    A Merit Badge Counselor is a registered status in a local 
                    council of the Boy Scouts of the Philippines.</p>
                  <p class="maintext">The counselor is the prime mover of Merit 
                    Badge scheme of the Boy Scouts of the Philippines. It is the 
                    counselor that provides the total Scouting experience and 
                    offer their time and expertise to help Scouts learn about 
                    the Merit Badge they are working on.</p>
                  <p class="maintext">The duty of the counselor is to assist Scouts 
                    in planning and meeting the requirements set by the Boy Scouts 
                    of the Philippines for a particular Merit Badge. He provides 
                    tools such as books and his own knowledge to give the Scout 
                    more depth into the subject. He coaches the Scout through 
                    interviews and demonstration on how to do the required skills 
                    of the craft, business, or hobby. Ultimately, he determines 
                    whether or not the Scout is qualified for the merit badge 
                    and certifies the Scout.</p>
                  <p class="maintext">Counselors are registered adult leaders 
                    of the Boy Scouts of the Philippines and are certified to 
                    be counselors within the local council or district they are 
                    registered in. Technically, Merit Badge Counselors may not 
                    work outside the jurisdiction of the council or district they 
                    are registered to serve as counselors. Registration is renewed 
                    annually.</p>
                  <h2>Frequently Asked Questions</h2>
                  
              <blockquote> 
                <p><strong><span class="bluetext">How much time does a counselor 
                  spend with a Scout earning one Merit Badge?<br />
                  </span> </strong><span class="maintext">It may be as short as 
                  two meetings or as many as four or five, with each meeting lasting 
                  from 20 to 30 minutes. The number of meetings all depends on 
                  how difficult the requirements are and the ability of the Scout 
                  to meet those requirements. The entire earning process can last 
                  from a few days to one or two months. The counselor need not 
                  be present while the Scout work on the requirement, unless it 
                is explicity stated that the counselor needs to be.</span></p>
                <p><strong><span class="bluetext">I have a college degree. Can 
                  I serve as a Merit Badge Counselor?<br />
                  </span> </strong><span class="maintext">Yes of course. Although 
                  there are no educational requirement to be a counselor, proven 
                  know-how is excellent. But having a degree doesn't qualify you 
                  automatically, you must register to be a counselor with a local 
                district or council.</span></p>
                <span class="maintext"><strong></strong></span> 
                <p><strong><span class="bluetext">How many Merit Badges can I 
                  be a counselor of?<br />
                  </span> </strong><span class="maintext">As many as you are qualified 
                to.</span></p>
                <p><span class="bluetext"><strong>Do I need to take a qualifying 
                  exam before I am certified as a counselor?<br />
                  </strong></span><span class="maintext">The Boy Scouts of the 
                  Philippines does not require tests for counselors. In the case 
                  of the swimming and lifesaving merit badges, where it requires 
                  Red Cross of BSP aquatic school recognition, the qualifying 
                  test would be an indirect requirement. Generally, there are 
                no exams required.</span></p>
                <p><span class="bluetext"><strong>As a counselor, can I add, modify, 
                  or omit a requirement?<br />
                  </strong></span><span class="maintext">No. A Merit Badge Counselor 
                  has no authority to do so. The authority lies with the local 
                  council's advancement committee. Doing so without approval may 
                cause problems for the Scout.</span></p>
              </blockquote>
