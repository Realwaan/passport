                  <h1>How to Earn a Merit Badge</h1>
<span class="maintext">
              <ol type="1">
                <li>Determine what Merit Badge is required. It is ideal to begin with the standard and required Merit Badges as it offers the basic and core foundation of the Scout Advancement with respect to Scout Ideals, Citizenship Training and Skills for Self-Reliance.</li>
                <p>                </p>
                <li>If it is an elective - select the one that interests you the 
                  most from the Group. If you cannot make a choice, ask your Troop Leader or Outfit Advisor to advise you.</li>
                <p>                </p>
                <li>After you have identified the Merit Badge/s you need or want, 
                  tell your Troop Leader or Outfit Advisor. He or she will help you 
                  fill up the <a href="/downloads/APP_MeritBadge.pdf">Merit Badge 
                  Application Form</a> and sign it.</li>
                <p>                </p>
                <li>The  Troop Leader or Outfit Advisor will help look for the right <a href="/htm/pub/mbcounseling.php">Merit 
                  Badge Counselor</a> and arrange your meeting for formal introduction.</li>
                <p>                </p>
                <li>You will have two or more meetings with your  <a href="/htm/pub/mbcounseling.php">Merit 
                  Badge Counselor</a>. 
                  <ol type="a">
                    <li>First Meeting: The Troop Leader or Outfit Advisor introduces the Scout to the Merit Badge Counselor and gives a brief background about his or her vocation or profession or his or her interest about the Merit Badge subject. The first meeting basically covers the scope, standards   and requirements of the Merit Badge subject. The Merit Badge Counselor gives short introduction   and basic knowledge or demonstrate a skill about the subject or topic. The Merit Badge Counselor   may suggest a book or any related literatures for further readings and study. This meeting   usually ends with an assignment or a project which the Scout should accomplish to show   and demonstrate knowledge and skills about the MB subject/s at hand. All concerned   parties agree and set date of the next (second) meeting. </li>
                    <li>Second Meeting: During the second meeting, the Scout will bring the completed   project and the Counselor will explain and discuss whether the Scout have studied and   complied with the requirements. If the Merit Badge Counselor is satisfied, he or she will sign the   Scout/s&rsquo; Merit Badge Application Form and certify that the Scout/s have successfully   earned the Merit Badge. If not, the Merit Badge Counselor will advise the Scout to do additional   tasks or assignments to satisfy the requirements. At this stage, all concerned party shall   decide whether another meeting is needed and specific date and time is set. </li>
                    <li> Third Meeting: This is usually the third and final meeting of the Scout with the Merit Badge   Counselor, preferably together with the Troop Leader or Outfit Advisor. This meeting sets everything on   the table as to compliance and standards for each Merit Badge required for a particular   Rank. By this time, requirements must have been completed and satisfied and the Merit   Badge Application Form together with the Passport is signed. Make sure to indicate the   dates when the Merit Badge is applied and earned.</li>
                  </ol>
                                    
                </li>
                <li>Once completed, make sure you asked your <a href="/htm/pub/mbcounseling.php">Merit Badge Counselor</a> and Troop Leader or Outfit Advisor to sign your Advancement Passport.
                  <p>                </p>
                </li>
                <li>In due time, your Troop Leader or Outfit Advisor will formally your Merit Badge in an appropriate ceremony.</li>
                <p>              </p>
              </ol>
	</span>
