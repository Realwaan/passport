              <h1>About the Logo </h1>
              <p align="justify" class="maintext"><img src="/img/images/logo_mbcp.gif" alt="MBCP" width="143" height="138" align="right" />Scouts work
                on requirements, earn merit badges, and are awarded ranks.
                This activity is fondly called as <strong>Advancement</strong> in
                Scouting. In an effort to best represent this in a logo, I figured
                that advancement is best represented by an upward motion signified
                by an arrow or a compass point. Also, since this website is mainly
                about merit badges, why not bound this arrow within a merit badge
                border? So, I did just that. Thus the Merit Badge Center logo
                is a merit badge patch symbolizing advancement.</p>
              <p class="maintext">The logo was designed and first used on May
                30, 2001.  </p>
