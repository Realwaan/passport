              <h1>Help!</h1>
              <h2>How to Use this Site</h2>
              <ol class="maintext">
                <li class="maintext"> 
                  <p>Navigate using the main navigation links found on the left-side
                    by selecting a topic: Advancement Ranks or Merit Badges.
                    Merit Badges are also grouped into Alphabetical Listing,
                    Eagle Scout Required, Specialist Ratings, and by Groups. </p>
                </li>
                <li class="maintext">
                  <p>Select the badge topic you need to earn or that interests 
                    you. Instructions how to earn a <a href="/htm/pub/mblearnearn.php">Merit 
                    Badge</a> and <a href="/htm/specialist.php">Specialist Rating</a> 
                    are also provided in this website.</p>
                </li>
                <li> 
                  <p>Once you have selected a badge you will be presented with 
                    the badge requirement on the main content area. The content 
                    area will also provide a scanned picture of the actual badge. 
                    Eagle required Merit Badges will have the word &quot;EAGLE 
                    REQUIRED&quot; next to the picture. There will be additional 
                    links on the left-side navigation bar. The additional links 
                    are categorized as follows:</p>
                  <ol class="maintext" type="a">
                    <li> 
                      <p><strong>Resource Links </strong>provides third-party 
                        websites with information to assist Scouts and Scouters 
                        in completing one or more of the requirements for the 
                        badge. Third-party websites are websites external to the 
                        Merit Badge Center.</p>
                    </li>
                    <li> 
                      <p><strong>Local Links</strong> provides on-us web pages 
                        or adobe acrobat documents to assist Scouts and Scouters 
                        in completing one or more of the requirements for the 
                        badge. Under local links are also downloadable <strong>Badge 
                        Worksheets</strong> that will assist you in completing 
                        the requirements and make the process a bit organized.</p>
                    </li>
                    <li> 
                      <p><strong>Specialized Links</strong> provides third-party 
                        websites that are grouped into a specific category or 
                        topic, such as &quot;Civic Organizations&quot; or &quot;Scouting 
                        Events&quot;.</p>
                    </li>
                    <li> 
                      <p><strong>Website Tools</strong> will have a link to a 
                        printable version of the requirements and an e-mail address 
                        to report broken links</p>
                    </li>
                  </ol>
                </li>
              </ol>
              <h2>Unable to Open Publications</h2>
              <p class="maintext">All our publications are in Adobe Acrobat format. 
                You will need to <a href="http://www.adobe.com/products/acrobat/readstep2.html" target="_blank">download 
                the Adobe Acrobat Reader</a> to open these documents. Downloading 
                this reader is free from Adobe. </p>
              <p class="maintext">If you need these documents in other formats, 
                please <a href="/htm/contactus/contactus.php">contact us</a>.</p>
              <h2>Unable to Open Resource Links</h2>
              <p class="maintext">There are several possible reasons. You may 
                have a &quot;Pop-Up Blocker&quot; that is enabled. You will need 
                to allow Merit Badge Center as on of the web site you will allow 
                pop-up for. Windows XP SP2 update has its own pop-up blocker for 
                Internet Explorer in addition to those you are alrady using from 
                Yahoo or Google. You can set this under IE's &quot;Tools&quot; 
                menu.</p>
              <p class="maintext">Another possibility is that you are missing 
                some of the necessary Windows registry keys that enables new browser 
                window. Do the following to solve this problem:</p>
              <ol class="maintext">
                <li>Click the <strong>START </strong>button and select the <strong>RUN</strong> 
                  command.</li>
                <li>Type &quot;<strong>regsvr32 actxprxy.dll</strong>&quot; and 
                  click <strong>OK</strong>.</li>
                <li>A message box &quot;<strong>DllRegisterServer in actxprxy.dll 
                  succeeded</strong>&quot; and click <strong>OK</strong>.</li>
                <li>Do Step 1 and type &quot;<strong>regsvr32 shdocvw.dll</strong>&quot; 
                  and click <strong>OK</strong>.</li>
                <li>A message box &quot;<strong>DllRegisterServer in shdocvw.dll 
                  succeeded</strong>&quot; and click <strong>OK</strong>.<strong> 
                  </strong></li>
                <li>Restart Windows.</li>
              </ol>
              <h2>Unable to Open Compressed Files</h2>
              <p class="maintext">If you are using Microsoft Windows version earlier 
                than XP, you will need an unzipping tool. You can download one 
                from <a href="http://www.winzip.com/" target="_blank">WinZip</a> 
                or <a href="http://www.pkware.com/" target="_blank">PKWare</a>.</p>
