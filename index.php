<h1 class="h2">New Senior Scout Passport Program</h1>
<p class="maintext">On May 23, 2019, the <a href="http://www.scouts.org.ph">Boy Scouts of the Philippines</a> issued <a href="/downloads/2019nmemo17.pdf">National Memorandum No. 37</a> with the <a href="/downloads/2019nmemo17-IRR-SS.pdf">Implementing Rules and Regulations of the new Senior Scout Advancement Scheme and Merit Badge System for My Trail Eagle Scout Passport</a>. To shorten it, I call it the &quot;Passport&quot; program. The new requirements are now online on our <a href="/htm/advancement.php">Advancement Rank Page</a>. Note: as per item # 6 on the memo, the revised Senior Scout Advancement Scheme takes effect immediately covering those newly registered Senior Scouts who will start their Scout Advancement for the Year 2019. Currently registered Senior Scouts whose Advancement Rank are in progress shall continue using the old Senior Scout Advancement Scheme.</p>
