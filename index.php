<p class="maintext">A revised list of <a href="/htm/specialist.php">Specialist Ratings</a> is available based on list released by Yasser F. Sarona for the Boy Scouts of the Philippines. This update is as of January 5, 2016.</p>
<h1 class="h2">Revised Eagle Scout Application Procedure</h1>
<p class="maintext">Download the <a href="/downloads/2012nmemo15.pdf">National Memo No. 51, s 2012</a>. Revised application for <a href="/downloads/APP_EagleScout_2012.pdf">Eagle Scout</a>, <a href="/downloads/APP_MeritBadge_2012.pdf">Merit Badge</a>, and <a href="/downloads/APP_MeritBadgeCounselor_2012.pdf">Merit Badge Counselor</a> are in our <a href="/html/forms.php">Forms Section</a>.</p>
