              <h2>Ten Outstanding Boy Scouts of the Philippines</h2>
              <p class="fineprint"><strong>Source:</strong> Murice Albert Figueras (TOBS2002). Medallion scan by Joshua C. Young (TOBS2006). </p>
              <p class="maintext"><img src="/img/medal_tobs.jpg" alt="TOBS Medallion" width="192" height="247" hspace="10" vspace="10" border="0" align="right" />The
                search for the Ten Outstanding Boy Scouts of the Philippines
                (TOBSP) was conceived in 1989 by the late Education Secretary
                and BSP National President Isidro D. Cari&ntilde;o
              as one of the avenues to propagate his values educaiton program.</p>
              <p class="maintext">The search aims to give due recognition to
                the most outstanding Scouts who epitomize sterling qualities
                of school and community leadership, academic excellence, and
                good moral conduct, thereby serving as role models to the youth.</p>
              <p class="maintext">The quest starts at the council level where
                Scouts contend to participate in the regional eliminations and
                to eventually represent his region in the national finals in
                Manila. After a thorough scrutiny of the candidates' credentials,
                they undergo rigid interview with a distinguished panel of judges.
                Criteria for judging include leadership potential, non-academic
                awards and recognition, academic proficiency, personality, and
                verbal communication.</p>
              <h2>Search Guidelines</h2>
              <ul class="maintext">
                <li>The search is open to Boy Scouts who are Filipino citizens,
                  currently registered, and must be in his third year or fourth
                  year in high school.</li>
                <li>The search will be conducted on district/area, local, regional,
                  and national levels. Search must start from the district/area
                  level to involve greater number of Scouts.</li>
                <li>Selection Committees from District/Local Council/Regional
                  are composed of:
                  <ul>
                    <li>DepEd Director/Superintendent/Supervisor;</li>
                    <li>Program Commissioner;</li>
                    <li>Training Commissioner;</li>
                    <li>President of Any Civic Organization; and</li>
                    <li>A Representative from the Sponsor.*</li>
                  </ul>
                </li>
                <li>The National Board of Judges are composed of the TOBS Committee
                  with members from the:
                  <ul>
                    <li>National Executive Board of the BSP;</li>
                    <li>DepEd;</li>
                    <li>The Sponsor*; and</li>
                    <li>The Ten
                        Outstanding Boy Scouts of the Philippines Association
                      (TOBSPA).</li>
                  </ul>
                </li>
                <li>Each region is allocated a number of candidates they are
                  allowed to send into the national finals, this allocation will
                  depend on the size of their youth membership.</li>
                <li>Criteria: 
                  <ul>
                    <li>Performance:
                      <ul>
                        <li>Community Service</li>
                        <li>Scholastic Achievements </li>
                        <li>Scouting Participation</li>
                      </ul>
                    </li>
                    <li>Character:
                      <ul>
                        <li>Living up to the Scouting Ideals</li>
                        <li>Observance of Religious Obligations</li>
                      </ul>
                    </li>
                    <li>Interview:
                      <ul>
                        <li>Communication Skills</li>
                        <li>Personality</li>
                      </ul>
                    </li>
                  </ul>
                </li>
                <li>Scouting advancement is only a part of the criteria and will
                  not be the only basis for a Scout to be named &quot;Outstanding&quot;.
                  Community Service and Scholastic Achievements   bear weight.</li>
                <li>Winners of the search are presented the TOBS medal, cash,
                  and scholarship during the national awarding ceremonies. No
                  less than the BSP National President awards the Ten Outstanding
                  Boy Scouts. They automatically are inducted as members of the
                  TOBSPA. </li>
              </ul>
              <p class="maintext">For more information, visit the <a href="http://www.geocities.com/tobspa_scout/" target="_blank">TOBSPA
              website</a>.</p>
              <p class="fineprint">____________________<br />
              *Coca-Cola Export Corporation has been the sole sponsor of the
                search since 1998. </p>
