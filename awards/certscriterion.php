              <h1>Merit Badge Center Certificates</h1>
              <p class="maintext">In an effort to recognize the small
                deeds by individuals and Scouts that can possibly ripple through
                the water and create a huge impact, the Merit Badge Center informally
                awards certificates. <a href="/htm/awards/certs.php?tp=list">See
                the list of awardees</a>.</p>
              <h2><span class="maintext"><img src="/img/logo_sample_accr.jpg" alt="Accrediation" width="110" height="110" hspace="5" vspace="5" align="right" /></span>Accreditation</h2>
              <p class="maintext">Recognizing
                Scouting websites, particularly those by Filipinos or for Filipinos,
                that maintains a level of quality. It is the purpose of this
                award to encourage others to design better and useful websites
                that will truly benefit their unit, institution, or Scouting
                in general.</p>
              <p class="maintext">To receive the accreditation, websites must
              satisfy the following guidelines: </p>
              <ul class="maintext">
                <li>
                  <p>The website must be included in our list of <a href="/htm/mbcplinks.php">Sponsored
                    Links</a>. <em>You may send your site for consideration into
                    the sponsored links through our <a href="/htm/submitasite.php">Submit
                    a Site</a> page</em>. </p>
                </li>
                <li>
                  <p>The website must pass against our
                    <a href="/htm/submitasite.php">Acceptable Web Site/Page Policy</a>.                    </p>
                </li>
                <li>
                  <p>The site must not be created for the sole purpose of displaying
                    almost perpetual static information about the group or Scouting
                    in general (i.e., Scout Oath and Law, Motto, Slogan, Membership
                    or Leadership Roster, etc.). It must contain timely information
                    such as, but not limited to, upcoming activities, recent
                    group publication, group achievements and/or projects, etc. </p>
                </li>
                <li>
                  <p>The website must be online for at least one-(1) year with
                    evidence that the pages are updated regularly and as necessary. </p>
                </li>
              </ul>
              <p class="maintext">All accredited websites are given the opportunity to display their accrediation using an custom accrediation image that may be used as a link to provide proof of their accreditation. Accredited websites will be reviewed on a regular basis to ensure they continually satisfy the guidelines. </p>
              <h2>Appreciation</h2>
              <p class="maintext">Recognizing that the success of the Merit Badge
                Center would never be possible without the direct and indirect
                contributions of individuals or groups. The Certificate of Appreciation
                is awarded to people or groups who have contributed in great
                lengths bringing the website to what it is today. </p>
              <h2>Commendation</h2>
              <p class="maintext">In recognition of noteworthy action(s) by an
                individual or a group that, in the opinion of the Merit Badge
                Center, exemplifies the values of Scouting embodied by the Scout
                Oath and Law. The Merit Badge Center does not actively seek individuals or groups on its own and rely on nominations or recommendations coming for various individuals. All submission are reviewed thoroughly. Submit your nomination to <a href="mailto:commendations@meritbadge.org.ph">Commendations@meritbadge.org.ph</a>. </p>
              <p class="maintext">To receive the commendation, an individual
                or a group must satisfy the following guidelines:</p>
              <ul class="maintext">
                <li>
                  <p><strong>The individual or group must have demonstrated action(s)
                      that exemplifies the points of the Scout Oath and Law.</strong> It
                      is important to remember that the commendation is being
                      directed to the demonstrated action(s), it bears no importance
                      who the group or individual is. The group or individual
                      need not be successful in achieving whatever they aimed
                      to achieve, so long as the purpose and objective behind
                      such action(s) is commendable. The Merit Badge Center shall
                      not engage in judging any perceived motivation of such
                      action(s) in cases where there is a perceived political
                      motivation. The action however must be deeply rooted in
                      the foundations of moral principles and reason. Action
                      for action's sake is empty and action done irrationally
                      is not worthy of commendation. It is equally important
                      that, while satisfying some points of the Scout Oath and
                      Law, the action does not violate some other points of the
                      Oath and Law. Purpose and objective is not enough for commendation
                      if the method(s) or action(s) taken to achieve such purpose
                      or objective is flawed.</p>
                </li>
              </ul>
              <ul class="maintext">
                <li><strong>There must be proof of such act or action </strong> through
                  newspaper accounts or other material that will provide substance
                  to the claim of such an action and the foundation behind
                the action if necessary. </li>
              </ul>
              <h2><span class="maintext"><img src="/img/cert_eaglecommend.jpg" alt="Sample Eagle Commendation" width="200" height="150" hspace="10" vspace="10" align="right" /></span>Eagle Scout Commendation</h2>
              <p class="maintext">Given to those who have completed the requirements
                for the Eagle Scout. To receive the commendation furnish the
                following copies:</p>
              <ul class="maintext">
                <li>Complete Name of the Eagle Scout (exactly how you want it
                to appear on the certificate) </li>
                <li>Most recent BSP Membership Card</li>
                <li>Eagle Scout Card Certificate</li>
                <li>Eagle Scout Commendation Letter from the National Headquarters</li>
                <li>Approved Application for Eagle Scout (BSP Form No. 10) Front
                  and Back</li>
              </ul>
              <p class="maintext">Send request including the enclosures to <a href="mailto:EagleCommendations@meritbadge.org.ph">EagleCommendations@meritbadge.org.ph</a>.
                Have all attachments in compressed JPEG format with a combined
                size no more than 1MB. </p>
