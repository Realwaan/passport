                  <h1>Specialist Ratings Source </h1>
                  <p class="maintext"><strong>NOTE: This is no longer applicable to the current Specialist Ratings list on the website updated as of January 5, 2016. The new list is based on information released by Yasser F. Sarona of the BSP. This method below explains how the OLD list was derived prior to the new list now available online.</strong></p>
                  <p class="maintext">Several ocassions I have been asked for
                    the source of the list of Specialist Ratings listed on the
                    website. So here it is.</p>
                  <p class="maintext">The source of the compiled list comes from
                    the <strong>13 and Above Handbook</strong> and the <strong>Advancement
                    and Merit Badge Handbook</strong> both published by the <a href="http://www.phiscout.org">Boy
                    Scouts of the Philippines (BSP)</a>. As everyone is aware,
                    each publication presents its own list of Specialist Ratings
                    and since there is no official BSP guideline concerning which
                    one is correct it is a valid assumption that both list are
                    valid lists. </p>
                  <p class="maintext">Prior to publishing a compiled list, a
                    series of elimination needed to take place:</p>
                  <ul class="maintext">
                    <li>
                      <p><strong>Removing all merit badges explicitly required
                        for Eagle Scout. </strong>By saying explicitly, it refers
                        to merit badges that leaves a Scout no choice but to
                        earn them. For example, <a href="/htm/meritbadges.php?mb=safety">Safety</a> is
                        explicitly required since before you become an Explorer
                        Scout you will need to have this merit badge, while <a href="/htm/meritbadges.php?mb=ecology">Ecology</a> is
                        not explicity required since you can take <a href="/htm/meritbadges.php?mb=treefarming">Tree
                        Farming</a> instead. We remove these merit badges since
                        these merit badges can no longer be used as one of the
                        three merit badges you use to earn your specialist rating,
                        otherwise you will end up with less than 21 merit badges
                        for Eagle Scout.</p>
                    </li>
                    <li>
                      <p><strong>Removing all merit badges with no official BSP
                        requirements attached to them.</strong> In short, these
                        merit badges are not found in the &quot;Advancement and Merit
                        Badge Handbook&quot;. We call these the <a href="/htm/mbinworks.php">Merit
                        Badges in the Works. </a></p>
                    </li>
                    <li><strong>Removing specialist ratings that cannot be completed. </strong>After
                      removing merit badges from the list, some specilist ratings
                      are left with less than three merit badges under them.
                      Obviously, a specialist rating can only be earned after
                      earning three merit badges and completing a related service
                      project. We call these the <a href="/htm/specialist_unworkable.php">Unworkable
                      Specialist Ratings</a>. </li>
              </ul>
                  <p class="maintext">After all these eliminations, a <a href="/htm/specialist.php">finalized
                    list of specialist ratings</a> is published on this website. </p>
                  <h2>Canonical Source</h2>
                  <p class="maintext">As far as the process was concerned, it
                    was (and is) always important to use what I call &quot;BSP Canonical
                    Publications&quot;. What I refer to as canonical are BSP publications
                    that are still in official cirulation and not in any way
                    discontinued or revoked. In short, these publications are
                    still sold at the National Scout Store in the National Office.</p>
                  <p class="maintext">So, the old <strong>Senior Scout Program
                      Guidebook</strong> will no longer be considered canon since
                      it has been discontinued. Same goes for the <strong>Service
                      Scout Series of Handbooks</strong> which were discontinued
                      in the early 1990s. </p>
                  <p class="maintext">Any BSP circulars, memos, and orders are
                    also considered as Canonical Publications so long as they
                    are not rendered void by other BSP circulars, memos, and
                    orders. </p>
